# MIMO - Autoatribuição

# A autoatribuição ocorre quando atribuímos a uma variável o próprio valor dela ou quando incrementamos ou decrementamos seu valor.

carteira = 3  # carteira vale 3
print(f'Agora a variável vale {carteira}')

carteira = carteira  # carteira continua valendo 3
print(f'Agora a variável ainda vale {carteira}')

carteira += 1  # Incrementa o valor da carteira em 1
print(f'Agora a variável vale {carteira}')

# O mesmo conceito se aplica a strings
nome = 'Maycon'
print(nome)

nome += ' Douglas'  # Concatena 'Douglas' ao nome existente
print(nome)
