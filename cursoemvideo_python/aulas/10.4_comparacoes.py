# MIMO - Comparações

# Operador 'Maior que' (>): Retorna True se o valor da esquerda for maior que o da direita, caso contrário, retorna False.
print(2 > 1)  # True
print(1 > 2)  # False

# Operador 'Menor que' (<): Retorna True se o valor da esquerda for menor que o da direita, caso contrário, retorna False.
print(1 < 2)  # True
print(2 < 1)  # False

# Operador 'Maior ou igual (>=)': Retorna True se o valor da esquerda for maior ou igual ao da direita, caso contrário, retorna False.
print(2 >= 2)  # True

# Operador 'Menor ou igual (<=)': Retorna True se o valor da esquerda for menor ou igual ao da direita, caso contrário, retorna False.
print(2 <= 2)  # True

# Podemos usar variáveis para realizar as comparações, usando seus valores como referência.
v1 = 1
v2 = 5
print(v1 > v2)  # False
print(v1 < v2)  # True

# Também podemos salvar o resultado de uma comparação em uma variável.
comp = v1 > v2
print(comp)  # Exibe False como resultado da comparação

# Strings também podem ser comparadas.
# Retorna True, pois as strings são diferentes
print('online' != 'offline')
