# MIMO - Instrução type()

# Usamos a instrução type() para verificar o tipo de dado de uma variável.

# Podemos combinar com a instrução print() para exibir esse resultado no console: print(type(variavel))
variavel = 5
print(variavel)
tipo = type(variavel)
print(tipo)

# O resultado é uma saída que indica o tipo de dado da variável:
# <class 'bool'> para booleanos True e False
# <class 'int'> para números inteiros (sem decimais)
# <class 'float'> para números de ponto flutuante (com decimais)
# <class 'str'> para strings (cadeias de caracteres)
