# MIMO - VARIÁVEIS

# As variáveis desempenham um papel fundamental na programação, permitindo que os programas armazenem e manipulem informações.

# Criando Variáveis:
# Para criar uma variável, você deve escolher um nome descritivo para ela, seguido do operador de atribuição (=) e, em seguida, o valor que você deseja armazenar na variável. Por exemplo:
exemplo = 'Valor'

# Nomes de Variáveis:
# Os nomes das variáveis não podem conter espaços, então usamos o sublinhado (_) para conectar palavras em nomes de variáveis, seguindo o padrão chamado de "snake_case." Isso melhora a legibilidade do código. Por exemplo:
outro_exemplo = 'Outro valor'

# Nomes Descritivos:
# É essencial escolher nomes descritivos para suas variáveis, de modo que o código seja fácil de entender. Por exemplo, em vez de usar nomes genéricos como `x` ou `y`, você pode usar nomes mais informativos como `ano_atual` para armazenar o ano atual.

# Tipos de Dados:
# As variáveis podem armazenar valores de diferentes tipos de dados, como strings, números inteiros, números de ponto flutuante, booleanos, entre outros. Por exemplo:
tipo_1 = 'Letras, números (2) e caracteres especiais (@)'

# Variáveis Mutáveis:
# As variáveis são chamadas de "mutáveis" porque seus valores podem ser alterados ao longo do programa. Você pode atualizar os valores das variáveis, como no exemplo em que a variável `nome` inicialmente contém `'Maycon'` e depois é atualizada para `'Douglas'`.

# Atribuição de Variáveis:
# Você também pode atribuir o valor de uma variável a outra. No exemplo, `outro_nome` recebe o valor de `nome`, fazendo com que `outro_nome` contenha o mesmo valor que `nome` (nesse caso, `'Douglas'`).

# Entender o conceito de variáveis é fundamental para a programação, pois elas são usadas para armazenar dados e informações que podem ser manipulados e processados pelo programa. Escolher nomes descritivos e compreender como atualizar e atribuir valores são habilidades importantes ao trabalhar com variáveis em programação.
