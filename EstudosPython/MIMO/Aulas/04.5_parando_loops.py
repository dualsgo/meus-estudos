# Usando loops for, podemos escrever programas com muito menos código e facilitar o entendimento de outros programadores.

# Para criar um loop for, adicionamos a palavra-chave for, uma variável como i, a palavra in e, finalmente range() - for i in range():

# Em um loop for, podemos especificar quantas vezes gostaríamos que nosso loop fosse executado com a instrução range()

# Adicionar um número como 6, dentro de range() significa que ele fará uma iteração sobre o bloco de código 6 vezes (0 a 5)

for i in range(6):
    print(i)

# A variável antes de in, nesse caso, i, é a variável de contador. Conta em quan=l repetição do loop estamos atualmente